const createBoard = () => {
  for (let i = 0; i < layout.length; i++) {
    // create a square
    const square = document.createElement('div')
    // put square into grid
    grid.appendChild(square)
    // add square into squares array   
    squares.push(square)

    switch (layout[i]) {
      case 0:
        square.className = 'pac-dot'
        break;
      case 1:
        square.className = 'wall'
        break;
      case 3:
        square.className = 'power-pellet'
        break;
      default:
        break;
    }
  }
}
createBoard()

